public class tablas {
    public static void main(String[] args) {

        for (int i = 0; i < 11; i++) {

            System.out.println("Tabla del multiplicar de " + i);

            for (int j = 0; j < 11; j++) {
                System.out.println(i + "x" + j + "=" + i * j);
            }
        }


    }
}
